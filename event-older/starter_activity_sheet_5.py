from turtle import *

    
def triangle():
    pencolor("black")
    for counter in range(1):
        fillcolor("Blue")
        begin_fill()
        pencolor("Black")
        forward(120)
        right(120)
        forward(120)
        right(120)
        forward(120)
        right(200)
    
for counter in range(9):
    triangle()
